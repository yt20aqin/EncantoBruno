                try {
                    AssetDatabase.StartAssetEditing();

                    string harrietmcpherson = Path.GetFullPath(".") + Path.DirectorySeparatorChar + "ProjectSettings";
                    UnityGuidRegenerator regenerator = new UnityGuidRegenerator(harrietmcpherson);
                    regenerator.RegenerateGuids();
                      harrietmcpherson = Path.GetFullPath(".") + Path.DirectorySeparatorChar + "Assets";
